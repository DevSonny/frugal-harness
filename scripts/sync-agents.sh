#!/usr/bin/env bash
set -euo pipefail

CONFIG_FILE="$HOME/.frugal-harness/config.sh"
if [ -f "$CONFIG_FILE" ]; then
  source "$CONFIG_FILE"
else
  FRUGAL_MAIN="claude"
  FRUGAL_HELPERS="codex,agy"
  FRUGAL_DEPLOY_CLAUDE="yes"
fi

CORE="$HOME/.claude/shared/harness-core.md"
OUT_CODEX="$HOME/.codex/AGENTS.md"
OUT_AGY="$HOME/.gemini/config/AGENTS.md"
OUT_CLAUDE="$HOME/.claude/CLAUDE.md"

if [[ ! -f "$CORE" ]]; then
  echo "Missing core: $CORE" >&2
  exit 1
fi

timestamp="$(date '+%Y-%m-%dT%H:%M:%S%z')"

generate_agent_md() {
  local agent_name="$1"
  local is_main="$2"
  local out_file="$3"
  
  local wrapper_file
  if [ "$is_main" = "1" ]; then
    wrapper_file="$HOME/.claude/shared/${agent_name}-wrapper-main.md"
  else
    wrapper_file="$HOME/.claude/shared/${agent_name}-wrapper-helper.md"
  fi

  if [[ -f "$wrapper_file" ]]; then
    mkdir -p "$(dirname "$out_file")"
    {
      if [ "$is_main" = "1" ]; then
        echo "# frugal-harness - $agent_name (Main Handler)"
      else
        echo "# frugal-harness - $agent_name (Helper / Executor)"
      fi
      echo "# AUTO-GENERATED by $(basename "$0") on $timestamp"
      echo "# Do not edit directly. Edit harness-core.md or ${agent_name}-wrapper-*.md."
      echo
      cat "$CORE"
      echo
      echo "---"
      echo
      
      local mf="${FRUGAL_AGY_MODEL_FAST:-Gemini 3.5 Flash (Medium)}"
      local mb="${FRUGAL_AGY_MODEL_BASIC:-Gemini 3.1 Pro (Low)}"
      local mc="${FRUGAL_AGY_MODEL_COMPLEX:-Gemini 3.1 Pro (High)}"
      local ma="${FRUGAL_AGY_MODEL_ARCH:-Claude Opus 4.6 (Thinking)}"
      local md="${FRUGAL_DOCS_AGY_MODEL:-Gemini 3.1 Pro (Low)}"
      sed \
        -e "s|Gemini 3.5 Flash (Medium)|$mf|g" \
        -e "s|Gemini 3.1 Pro (Low)|$mb|g" \
        -e "s|Gemini 3.1 Pro (High) 또는 Claude Sonnet 4.6 (Thinking)|$mc|g" \
        -e "s|Gemini 3.1 Pro (High) or Claude Sonnet 4.6 (Thinking)|$mc|g" \
        -e "s|Gemini 3.1 Pro (High)|$mc|g" \
        -e "s|Claude Opus 4.6 (Thinking)|$ma|g" \
        -e "s|configurable (FRUGAL_DOCS_AGY_MODEL env var)|$md|g" \
        -e "s|configurable (FRUGAL_DOCS_AGY_MODEL)|$md|g" \
        "$wrapper_file"

    } > "$out_file"
    echo "Wrote $out_file ($(wc -l < "$out_file") lines) [Main: $is_main]"
  fi
}

# 1. Generate Codex AGENTS.md
if [ "$FRUGAL_MAIN" = "codex" ]; then
  generate_agent_md "codex" "1" "$OUT_CODEX"
elif [[ "$FRUGAL_HELPERS" == *"codex"* ]]; then
  generate_agent_md "codex" "0" "$OUT_CODEX"
else
  # Remove if not main and not a helper
  rm -f "$OUT_CODEX"
fi

# 2. Generate agy AGENTS.md
if [ "$FRUGAL_MAIN" = "agy" ]; then
  generate_agent_md "agy" "1" "$OUT_AGY"
elif [[ "$FRUGAL_HELPERS" == *"agy"* ]]; then
  generate_agent_md "agy" "0" "$OUT_AGY"
else
  rm -f "$OUT_AGY"
fi

generate_claude_md() {
  local is_main="$1"
  local wrapper_type
  if [ "$is_main" = "1" ]; then
    wrapper_type="main"
  else
    wrapper_type="helper"
  fi

  local wrapper_file="$HOME/.claude/shared/claude-wrapper-${wrapper_type}.md"
  local role_label
  if [ "$is_main" = "1" ]; then
    role_label="orchestrator"
  else
    role_label="helper"
  fi

  if [[ ! -f "$wrapper_file" ]]; then
    echo "Warning: Missing $wrapper_file — skipping CLAUDE.md generation" >&2
    return
  fi

  mkdir -p "$(dirname "$OUT_CLAUDE")"
  {
    echo "# frugal-harness - Claude (${role_label})"
    echo "# AUTO-GENERATED by $(basename "$0") on $timestamp"
    echo "# Do not edit directly. Edit harness-core.md or claude-wrapper-*.md."
    echo
    echo "@./shared/harness-core.md"
    echo

    local mf="${FRUGAL_AGY_MODEL_FAST:-Gemini 3.5 Flash (Medium)}"
    local mb="${FRUGAL_AGY_MODEL_BASIC:-Gemini 3.1 Pro (Low)}"
    local mc="${FRUGAL_AGY_MODEL_COMPLEX:-Gemini 3.1 Pro (High)}"
    local ma="${FRUGAL_AGY_MODEL_ARCH:-Claude Opus 4.6 (Thinking)}"
    local md="${FRUGAL_DOCS_AGY_MODEL:-Gemini 3.1 Pro (Low)}"
    sed \
      -e "s|Gemini 3.5 Flash (Medium)|$mf|g" \
      -e "s|Gemini 3.1 Pro (Low)|$mb|g" \
      -e "s|Gemini 3.1 Pro (High) 또는 Claude Sonnet 4.6 (Thinking)|$mc|g" \
      -e "s|Gemini 3.1 Pro (High) or Claude Sonnet 4.6 (Thinking)|$mc|g" \
      -e "s|Gemini 3.1 Pro (High)|$mc|g" \
      -e "s|Claude Opus 4.6 (Thinking)|$ma|g" \
      -e "s|configurable (FRUGAL_DOCS_AGY_MODEL env var)|$md|g" \
      -e "s|configurable (FRUGAL_DOCS_AGY_MODEL)|$md|g" \
      "$wrapper_file"
  } > "$OUT_CLAUDE"
  echo "Wrote $OUT_CLAUDE ($(wc -l < "$OUT_CLAUDE") lines) [Role: ${role_label}]"
}

# 3. Generate CLAUDE.md
if [ "${FRUGAL_DEPLOY_CLAUDE:-yes}" = "yes" ] || [ "$FRUGAL_MAIN" = "claude" ]; then
  if [ "$FRUGAL_MAIN" = "claude" ]; then
    generate_claude_md "1"
  else
    generate_claude_md "0"
  fi
else
  rm -f "$OUT_CLAUDE"
  echo "Removed $OUT_CLAUDE (not main handler and deploy disabled)."
fi
